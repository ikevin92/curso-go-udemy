package main

import "fmt"

func main() {
	a := 2

	// a = a + 2

	// fmt.Println(a)

	// Operadores de asignación

	// suma en asignación
	a += 2
	fmt.Println(a)

	// resta en asignación
	a -= 2
	fmt.Println(a)

	// multiplicación en asignación
	a *= 2
	fmt.Println(a)

	// división en asignación
	a /= 2
	fmt.Println(a)

	// módulo en asignación
	a %= 2
	fmt.Println(a)

}
